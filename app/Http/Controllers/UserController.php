    public function following($id){
        $user = Auth::user();
        $users = User::whereHas('followed', function($query) use ($id){
            $query->where('follower_id', '=', $id);
        })->get();
        return view('user.show_follow', compact('users', 'user'));
    }

    public function followers($id){
